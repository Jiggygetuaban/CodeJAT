public boolean recoverycheck() {
    dbConnectors dbc = new dbConnectors();
    Session sess = Session.getInstance();

    try {
        // First: get the user info by username
        String queryUser = "SELECT * FROM tbl_users WHERE u_username = '" + uname.getText() + "'";
        ResultSet resultUser = dbc.getData(queryUser);

        if (resultUser.next()) {
            usrname = resultUser.getString("u_username");
            uid = resultUser.getString("u_id");

            // Now get the recovery data using u_id as foreign key
            String queryRecovery = "SELECT * FROM tbl_recovery WHERE uid = '" + uid + "'";
            ResultSet resultRecovery = dbc.getData(queryRecovery);

            if (resultRecovery.next()) {
                ans1 = resultRecovery.getString("r_answer1");
                ans2 = resultRecovery.getString("r_answer2");
                ans3 = resultRecovery.getString("r_answer3");

                return true;
            } else {
                JOptionPane.showMessageDialog(null, "No recovery data found for this user.");
                return false;
            }

        } else {
            JOptionPane.showMessageDialog(null, "Username not found.");
            return false;
        }

    } catch (SQLException ex) {
        System.out.println("SQL Error: " + ex);
        return false;
    }
}
   
    
    

    

